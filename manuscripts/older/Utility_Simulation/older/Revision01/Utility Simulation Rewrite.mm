  <body>
    <p style="line-height: 150%; text-align: justify" class="MsoNormal">
      The approach described here is designed to allow better use of existing summary data, in the absence of true individual level data. It does this by creating hypothetical individual level data as an intermediate stage. The approach is necessarily limited by the need to make assumptions about the statistical relationship between the summary data available and the individual level data. For <a>example, we assumed that the mean EQ-5D estimates for each mRS followed a normal distribution, </a><a language="JavaScript" id="_anchor_41" href="#_msocom_41" class="msocomanchor" onmouseout="msoCommentHide('_com_41')" name="_msoanchor_41" onmouseover="msoCommentShow('_anchor_41','_com_41')">[j41]</a>&#160;which is bounded between negative and positive infinity. EQ-5D utility scores show a negative lower bound whose value depends on the valuation tariff used and an upper ceiling (1.00) by construction, and so a statistical distribution where these bounds are applied may be more appropriate. We also know that EQ-5D data are only poorly approximated by the normal distribution, as the distribution of EQ-5D is typically known to be &#8216;multimodal&#8217;. <a>This represents a more general limitation common to a range of modelling approaches, </a>(13)&#160;rather than something specific to this approach. <a language="JavaScript" id="_anchor_42" href="#_msocom_42" class="msocomanchor" onmouseout="msoCommentHide('_com_42')" name="_msoanchor_42" onmouseover="msoCommentShow('_anchor_42','_com_42')">[j42]</a>
