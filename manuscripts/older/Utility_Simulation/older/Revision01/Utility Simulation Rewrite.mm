    <p style="line-height: 150%; text-align: justify" class="MsoNormal">
      This paper describes a simulation-based approach for mapping utility scores reported for a larger number of states onto a smaller number of states, where <a>the frequency of patients in each outcome state is also reported</a><a language="JavaScript" id="_anchor_3" href="#_msocom_3" class="msocomanchor" onmouseout="msoCommentHide('_com_3')" name="_msoanchor_3" onmouseover="msoCommentShow('_anchor_3','_com_3')">[j3]</a>&#160;. The method uses a data reduction technique that involves simulated data reconstitution as an intermediate stage. The method differs from regression-type mapping or cross-walking approaches in that it involves producing simple representations of the individual level data as an intermediate stage to mapping from a larger number to a smaller number of utility states. (1)
    </p>
