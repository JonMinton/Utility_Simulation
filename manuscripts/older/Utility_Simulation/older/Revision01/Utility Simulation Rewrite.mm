    <p style="line-height: 150%; text-align: justify" class="MsoNormal">
      <b>Background:</b>&#160;Disease severity is often measured using <a>different and </a><font size="8.0pt"><a language="JavaScript" id="_anchor_1" href="#_msocom_1" class="msocomanchor" onmouseout="msoCommentHide('_com_1')" name="_msoanchor_1" onmouseover="msoCommentShow('_anchor_1','_com_1')">[j1]</a>&#160;</font>incompatible systems of classification. This is problematic in economic evaluations if costs and consequences <a>(health related quality of life (HRQoL) </a><font size="8.0pt"><a language="JavaScript" id="_anchor_2" href="#_msocom_2" class="msocomanchor" onmouseout="msoCommentHide('_com_2')" name="_msoanchor_2" onmouseover="msoCommentShow('_anchor_2','_com_2')">[j2]</a>&#160;</font>are reported using <a>different systems of classification</a><font size="8.0pt"><a language="JavaScript" id="_anchor_3" href="#_msocom_3" class="msocomanchor" onmouseout="msoCommentHide('_com_3')" name="_msoanchor_3" onmouseover="msoCommentShow('_anchor_3','_com_3')">[j3]</a>&#160;</font>. Methods for mapping between categories are required to allow these different sources of evidence to be used. Using stroke as a case study, this paper describes a method for mapping HRQoL from more (seven) to fewer (three or four) categories in a way which does not underrepresent the additional uncertainty involved in this indirect use of evidence.
    </p>
