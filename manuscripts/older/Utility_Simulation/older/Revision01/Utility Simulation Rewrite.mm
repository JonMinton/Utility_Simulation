    <p style="line-height: 150%; text-align: justify" class="MsoNormal">
      <b>Methods:</b>&#160;An informal Bayesian simulation approach was used to estimate, with credible intervals (CrIs), the estimated mean HRQoLs of patients in each of three discrete states (dead, dependent stroke, independent stroke), given summary data reported for each of seven discrete states. The approach incorporates the known uncertainty about both the expected HRQoL in each of the seven states and known uncertainty about the proportion of patients in each of the seven categories. The three-state estimates produced were compared with older estimates to assess face validity.
