    <p style="line-height: 150%; text-align: justify" class="MsoNormal">
      We provide two case studies. In the first case study, we show how the approach is used for estimating utility multipliers associated with being in an independent or dependent state following a stroke, given utility estimates and proportions reported by Modified Rankin Scale (mRS), which has seven mutually exclusive stroke severity states. (2)&#160;In the second case study, we show how the approach can be extended to produce estimates for the mean utility values associated with <a>states on the Glasgow Outcome Scale (GOS) for </a><a language="JavaScript" id="_anchor_4" href="#_msocom_4" class="msocomanchor" onmouseout="msoCommentHide('_com_4')" name="_msoanchor_4" onmouseover="msoCommentShow('_anchor_4','_com_4')">[j4]</a>&#160;traumatic brain injuries. Both examples make use of the data reported in an <a>MDM paper </a><a language="JavaScript" id="_anchor_5" href="#_msocom_5" class="msocomanchor" onmouseout="msoCommentHide('_com_5')" name="_msoanchor_5" onmouseover="msoCommentShow('_anchor_5','_com_5')">[j5]</a>&#160;reporting mRS outcomes, but involve collapsing the seven states into three states in the first example, and four states in the second example. &#160;In the first example, a single source of data is used to estimate both the mean utilities within each of the mRS states and the proportion of patients in each state, whilst in the second example this source of data is used to estimate the mean utilities, but the proportions in each state are estimated using a different data source.
